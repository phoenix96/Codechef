#include<iostream>
#include<stdio.h>
int main()
{   long long int t,n,r,a,b,c;
    scanf("%lld",&t);
    while(t--)
    {
        scanf("%lld %lld",&n,&r);
        for(int i=0;i<n;i++)
        {
            scanf("%d %d %d",&a,&b,&c);
        }
        printf("0\n");
    }
    return 0;
}
